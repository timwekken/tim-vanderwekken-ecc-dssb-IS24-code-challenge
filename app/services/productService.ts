export const getProducts = () => {
  return Object.values(products);
};

export const getProduct = (productId: string) => {
  return products[productId];
};

export const getFilteredProducts = (filterName: keyof Product, filterValue: string) => {
  return Object.values(products).filter((product: Product) =>
    String(product[filterName]).toLowerCase().includes(filterValue.toLowerCase())
  );
};

export const addOrEditProduct = (product: Product) => {
  if (!product.id) {
    const uniqueId = Date.now() + "-" + Math.floor(Math.random() * 1000);
    product.id = uniqueId;
  }
  products[product.id] = product;
  return product;
};
